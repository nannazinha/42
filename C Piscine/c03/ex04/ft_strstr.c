char	*ft_strstr(char *str, char *to_find)
{
	int i;
	int j;

	i = 0;
	if (*to_find == '\0')
		return (str);
	while (str[i] != '\0')
	{
		j = 0;
		while ((str[i + j] != '\0') && (to_find[j] == str[i + j]))
		{
			j++;
			if (to_find[j + 1] == '\0')
				return (&str[i]);
		}
		i++;
	}
	return (0);
}
