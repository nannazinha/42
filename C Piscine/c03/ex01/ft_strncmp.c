int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int i;

	i = 1;
	if (n == 0)
		return (0);
	else
	{
		while ((i < n) && (*s1 == *s2))
		{
			if ((n == 0) || (*s1 == '\0'))
				return (0);
			s1++;
			s2++;
			i++;
		}
		if (*s1 == *s2)
			return (0);
		else
			return (*s1 - *s2);
	}
}
