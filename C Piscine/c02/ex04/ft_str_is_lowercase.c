int	ft_str_is_lowercase(char *str)
{
	int i;

	i = 0;
	if (*str == '\0')
		i = 1;
	else
	{
		while (*str != '\0')
		{
			if ((*str > 96) && (*str < 123))
				i = 1;
			else
			{
				i = 0;
				break ;
			}
			str++;
		}
	}
	return (i);
}
