#include <unistd.h>

void	ft_print_comb(void)
{
	char x;
	char y;
	char z;

	x = '0';
	while (x < '9')
	{
		y = (x + 1);
		while (y <= '9')
		{
			z = (y + 1);
			while (z <= '9')
			{
				write(1, &x, 1);
				write(1, &y, 1);
				write(1, &z, 1);
				write(1, ",", 1);
				write(1, " ", 1);
				z++;
			}
			y++;
		}
		x++;
	}
	write(1, "789\n", 4);
}
