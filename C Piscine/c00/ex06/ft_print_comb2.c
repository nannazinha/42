#include <unistd.h>

void	ft_print_comb2()
{
	char a;
	char b;
	char c;
	char d;

	a = '0';
	b = '0';
	d = '1';
	while (a <= '9')
	{
		while (b <= '9')
		{
			c = a;
			d = b + 1;
			while (c <= '9')
			{
				while(d <= '9')
				{
					write(1, &a, 1);
					write(1, &b, 1);
					write(1, " ", 1);
					write(1, &c, 1);
					write(1, &d, 1);
					write(1, ", ", 2);
					d++;
				}
				d = '0';
				c++;
			}
			b++;
		}
		a++;
		b = '0';
	}
}



