#include <unistd.h>
#include <stdio.h>

void	ft_putnbr(int nb)
{
	char a;
	int i;
	int b[0];
	int c;

	i = 0;
	while (nb != 0)
	{
		b[i] = nb % 10;
		nb /= 10;
		i++;
	}
	printf("%d", i);
	while (i >= 0)
	{
		c = b[i];
		a = c  + '0';
		write(1, &a, 1);
		i--;
	}
}
