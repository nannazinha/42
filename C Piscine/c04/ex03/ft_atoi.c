int	ft_atoi(char *str)
{
	int sig;
	int * nb;
	int i;

	sig = 1;
	nb = 0;
	i = 0;
	while (*str == " ")
		str++;
	if (*str == "-")
	{
		sig = sig * (-1);
		str++;
	}
	if (*str == "+")
		str++;
	if ((*str >= '0') || (*str <= '9'))
	{
		nb[i] =  *str + '0';
		i++;
	}
	else
		return (0);
	return (nb * sig);
}
